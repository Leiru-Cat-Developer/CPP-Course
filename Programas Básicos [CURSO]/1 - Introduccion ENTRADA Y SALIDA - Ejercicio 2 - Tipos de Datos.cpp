#include<iostream>
using namespace std;
int main(){
	/*
		TIPOS DE DATOS [VARIABLES]
		
		- INT
		- FLOAT
		- DOUBLE
		- CHAR
	*/
	int entero = 10;
	float flotante = 200.99;
	double doble = 200.111;
	char caracter = 'A';
	cout<<"VALOR ENTERO: "<<entero<<endl;
	cout<<"VALOR FLOTANTE: "<<flotante<<endl;
	cout<<"VALOR DOBLE: "<<doble<<endl;
	cout<<"VALOR CARACTER: "<<caracter<<endl;
	return 0;
}
